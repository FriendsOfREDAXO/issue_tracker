<h1>REDAXO Issue Tracker - Hilfe</h1>

<h2>Übersicht</h2>
<p>
Der REDAXO Issue Tracker ermöglicht es Ihnen, Anfragen, Probleme und Vorschläge zu verwalten. 
Sie können Issues erstellen, kommentieren, kategorisieren und deren Status verfolgen.
</p>

<h2>Dashboard</h2>
<p>
Das Dashboard zeigt Ihnen auf einen Blick:
</p>
<ul>
    <li>Anzahl offener Issues</li>
    <li>Anzahl Issues in Arbeit</li>
    <li>Anzahl geplanter Issues</li>
    <li>Anzahl erledigter Issues der letzten 30 Tage</li>
    <li>Issues nach Kategorie gruppiert</li>
    <li>Die 10 neuesten Issues</li>
</ul>

<h2>Neues Issue erstellen</h2>
<ol>
    <li>Klicken Sie auf "Neues Issue erstellen"</li>
    <li>Geben Sie einen aussagekräftigen Titel ein</li>
    <li>Beschreiben Sie das Problem oder den Wunsch detailliert</li>
    <li>Wählen Sie die passende Kategorie aus</li>
    <li>Setzen Sie die Priorität</li>
    <li>Optional: Weisen Sie das Issue einem Benutzer oder AddOn zu</li>
    <li>Optional: Fügen Sie Tags hinzu</li>
    <li>Klicken Sie auf "Speichern"</li>
</ol>

<h2>Issue bearbeiten</h2>
<p>
Klicken Sie in der Liste auf ein Issue oder auf den Bearbeiten-Button. 
Sie können alle Felder anpassen und den Status ändern.
</p>

<h2>Kommentare</h2>
<p>
Kommentare ermöglichen die Diskussion über ein Issue. 
Jeder berechtigte Benutzer kann Kommentare hinzufügen.
</p>
<p>
<strong>Interne Kommentare:</strong> Admins können Kommentare als "Intern" markieren. 
Diese sind nur für andere Admins sichtbar.
</p>

<h2>Status</h2>
<ul>
    <li><strong>Offen:</strong> Issue wurde erstellt und wartet auf Bearbeitung</li>
    <li><strong>In Arbeit:</strong> Issue wird aktuell bearbeitet</li>
    <li><strong>Geplant:</strong> Issue ist für die Zukunft eingeplant</li>
    <li><strong>Abgelehnt:</strong> Issue wird nicht umgesetzt</li>
    <li><strong>Erledigt:</strong> Issue wurde erfolgreich gelöst</li>
</ul>

<h2>Prioritäten</h2>
<ul>
    <li><strong>Niedrig:</strong> Kann warten</li>
    <li><strong>Normal:</strong> Normale Bearbeitung</li>
    <li><strong>Hoch:</strong> Sollte bald bearbeitet werden</li>
    <li><strong>Dringend:</strong> Muss schnell bearbeitet werden</li>
</ul>

<h2>Filter und Suche</h2>
<p>
Sie können Issues filtern nach:
</p>
<ul>
    <li>Status</li>
    <li>Kategorie</li>
    <li>Suchbegriff (durchsucht Titel und Beschreibung)</li>
</ul>

<h2>E-Mail-Benachrichtigungen</h2>
<p>
Das System sendet automatisch E-Mail-Benachrichtigungen bei:
</p>
<ul>
    <li>Neuen Issues</li>
    <li>Neuen Kommentaren</li>
    <li>Status-Änderungen</li>
    <li>Zuweisungen</li>
</ul>
<p>
<em>Hinweis:</em> E-Mail-Benachrichtigungen können in den Einstellungen aktiviert/deaktiviert werden.
</p>

<h2>Einstellungen (nur Admins)</h2>
<p>
Admins können:
</p>
<ul>
    <li>Kategorien verwalten (hinzufügen, bearbeiten, entfernen)</li>
    <li>E-Mail-Benachrichtigungen aktivieren/deaktivieren</li>
    <li>Absender-Name für E-Mails anpassen</li>
    <li>Broadcast-Nachrichten an alle Benutzer senden</li>
</ul>

<h2>Berechtigungen</h2>
<ul>
    <li><strong>issue_tracker[]:</strong> Basis-Zugriff auf den Issue Tracker</li>
    <li><strong>issue_tracker[issuer]:</strong> Kann Issues erstellen und kommentieren</li>
    <li><strong>admin:</strong> Vollzugriff inkl. Einstellungen und Broadcast-Funktion</li>
</ul>

<h2>Tipps</h2>
<ul>
    <li>Verwenden Sie aussagekräftige Titel für Issues</li>
    <li>Beschreiben Sie Probleme so detailliert wie möglich</li>
    <li>Nutzen Sie Tags zur besseren Organisation</li>
    <li>Aktualisieren Sie den Status regelmäßig</li>
    <li>Kommunizieren Sie über Kommentare</li>
</ul>
